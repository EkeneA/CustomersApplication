using CustomersApplication.Buisness.Customers;
using CustomersApplication.Buisness.WebApi;
using CustomersApplication.Core.Customers.Interfaces;
using CustomersApplication.Core.WebApi.Interfaces;

namespace CustomersApplication.Web
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            // Dependency Injection
            builder.Services.AddTransient<ICustomerService, CustomerService>();
            builder.Services.AddTransient<IWebApiService, WebApiService>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
